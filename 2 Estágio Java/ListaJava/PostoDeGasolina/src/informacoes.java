
public class informacoes {
	
	public float realizaCalculoGasolina(float qntLitros){
		
		float precoGasolina = 2.5f; 
		
		return qntLitros*precoGasolina;
	
		
	}
	
	
	
	public float realizaCalculoRefrigerante(int qntRefrigerante){
		int precoRefri = 3;
		
		
		
		
		return qntRefrigerante * precoRefri; 
	}
	
	
}
